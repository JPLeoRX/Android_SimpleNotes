package com.tekleo.simple_notes.backend;

import android.content.Context;

/**
 * Created by Leo on 03-Feb-16.
 */
public class Current
{
    public static Data DATA;
    public static Data SEARCH;

    //----------------------------------------------------------------------------------------------
    //------------------------------------ Search --------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public static void searchStart(String title) {
        SEARCH = DATA.search(title);
    }

    public static void searchEnd() {
        SEARCH = null;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------



    //----------------------------------------------------------------------------------------------
    //----------------------------------- Utility --------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public static void saveFile(Context context) {
        Data.saveToFile(context, DATA, "user_notes.sn");
    }

    public static void loadFile(Context context) {
        DATA = Data.loadFromFile(context, "user_notes.sn");
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------



    //----------------------------------------------------------------------------------------------
    //---------------------------------- Indicators ------------------------------------------------
    //----------------------------------------------------------------------------------------------
    public static boolean isReadyData() {
        return DATA != null;
    }

    public static boolean isReadySearch() {
        return SEARCH != null;
    }
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
    //----------------------------------------------------------------------------------------------
}
